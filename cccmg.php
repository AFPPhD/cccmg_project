<h2 id ="intro">Introduction</h2>
<p>This page is intended to help direct you to pieces and CD recordings you 
might like if you are new to classical music up to and including the casual collector 
like myself (People who want at most to find about 2 or 3 good recordings of a particular 
piece). The OTHER RECORDINGS section includes recordings I have not heard in their entirety 
or at all. Although I cannot recommend them whole heartedly myself, they come recommended 
from plenty of credible sources. These recommendations along with my own should help guide 
you to better informed and more detailed discussions on the internet.</p>

<h3>BBC Radio 3</h3>
<p>A good place to listen to mainly complete recordings of pieces that might interest you 
is Classical Collection 10.00 am - 1200pm (All times mentioned UK) weekdays. The Building
a Library section (approximately 9.30 - 10.30am) on CD review (usually 9.00am - 12.15pm)
Saturday morning, involves an expert sifting through the available recordings of a particular
piece to recommend their top recording. Through the Night, 1.00am - 7.00 am, mainly consists
of complete pieces, taken from live performances. The play list schedules can be found at
bbc.co.uk/radio3. You can also listen online.</p>

<h3>Classic FM</h3>
<p>If you want to listen to slow/quiet movements of pieces try Smooth Classics 10.00pm - 
2.00am. From 8.00 - 10.00pm Mon - Fri is The Full Works, where recordings of pieces are mostly 
played in full. (Any particular movements are generally played at any other time.)  You can listen 
online at their website classicfm.com. They also have a play list of what has been played.</p>

<h3>Radio Times Magazine (UK)</h3>
<p>It includes schedule lists and approximate timings for Classical Collection and Through the
Night, as well as telling you what the Building a Library featured work is. The schedule
for the Classic FM Full Works programmes is also given.</p>

